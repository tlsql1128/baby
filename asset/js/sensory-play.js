  function esc(s) {
    if (!s) return "";
    return String(s)
      .replace(/&/g, "&amp;")
      .replace(/</g, "&lt;")
      .replace(/>/g, "&gt;")
      .replace(/"/g, "&quot;");
  }

  function buildCard(d) {
    return (
      '<div class="sense-card" role="article">' +
      '<h3 class="sense-card__title">' +
      esc(d.t) +
      "</h3>" +
      '<ul class="sense-card__list">' +
      "<li><span>추천 월령</span> " +
      esc(d.m) +
      "</li>" +
      "<li><span>준비물</span> " +
      esc(d.need) +
      "</li>" +
      "<li><span>놀이 방법</span> " +
      esc(d.how) +
      "</li>" +
      "<li><span>소요 시간</span> " +
      esc(d.time) +
      "</li>" +
      "<li><span>주의사항</span> " +
      esc(d.care) +
      "</li>" +
      "</ul>" +
      "</div>"
    );
  }

  function fillPanels() {
    var keys = ["touch", "hear", "see", "smell", "taste"];
    var i;
    for (i = 0; i < keys.length; i++) {
      var k = keys[i];
      var list = DATA[k] || [];
      var html = [];
      var j;
      for (j = 0; j < list.length; j++) {
        html.push(buildCard(list[j]));
      }
      var el = document.getElementById("sense-body-" + k);
      if (el) {
        el.innerHTML = html.join("");
      }
    }
  }

  function setActiveTab(sense) {
    var nav = document.querySelectorAll(".sense-tab");
    var panels = document.querySelectorAll(".sense-panel");
    var a;
    for (a = 0; a < nav.length; a++) {
      var btn = nav[a];
      var isOn = btn.getAttribute("data-sense") === sense;
      if (isOn) {
        btn.classList.add("is-active");
        btn.setAttribute("aria-selected", "true");
        btn.setAttribute("tabindex", "0");
      } else {
        btn.classList.remove("is-active");
        btn.setAttribute("aria-selected", "false");
        btn.setAttribute("tabindex", "-1");
      }
    }
    for (a = 0; a < panels.length; a++) {
      var p = panels[a];
      if (p.getAttribute("id") === "panel-" + sense) {
        p.classList.add("is-active");
        p.removeAttribute("hidden");
      } else {
        p.classList.remove("is-active");
        p.setAttribute("hidden", "hidden");
      }
    }
  }

  function init() {
    fillPanels();
    setActiveTab("touch");
    var nav = document.querySelectorAll(".sense-tab");
    var n;
    for (n = 0; n < nav.length; n++) {
      nav[n].addEventListener("click", function () {
        var s = this.getAttribute("data-sense");
        if (s) setActiveTab(s);
      });
      nav[n].addEventListener("keydown", function (e) {
        var key = e.key;
        if (key === "Enter" || key === " ") {
          e.preventDefault();
          var s = this.getAttribute("data-sense");
          if (s) setActiveTab(s);
        }
      });
    }
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", init);
  } else {
    init();
  }
